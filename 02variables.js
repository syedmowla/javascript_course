/* Old way of naming variables uses var */

var firstName = 'Syed';

/* modern way of naming a variable uses let */

let firstName = 'Steven';

/* Can assign multiple variables in one line */

let firstName = 'Syed', lastName = 'Mowla'; /* This is hard to read, if there are a lot of lines*/

let message = 'Hello'; /* Initially sets the variable  message as Hello */ 
message = 'World!'; /* Reassigns the variable message as World */


